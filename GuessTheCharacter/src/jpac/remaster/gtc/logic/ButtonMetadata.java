package jpac.remaster.gtc.logic;

public class ButtonMetadata {

	private String puzzleAnswer;

	private int puzzleID;
	
	private String removedButtons;
	
	private String lockedAnswers;
	
	public String getPuzzleAnswer() {
		return puzzleAnswer;
	}

	public int getPuzzleID() {
		return puzzleID;
	}

	public String getRemovedButtons() {
		return removedButtons;
	}

	public String getLockedAnswers() {
		return lockedAnswers;
	}

}
